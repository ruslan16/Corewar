void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int				i;
	unsigned int	n;

	if (s && f)
	{
		i = 0;
		n = 0;
		while (s[i])
		{
			f(n, &s[i]);
			n++;
			i++;
		}
	}
}
